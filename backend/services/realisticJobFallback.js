  /**
   * Generate realistic private jobs for India (no API needed)
   */
  generateRealisticJobs({ location = 'India', keywords = '', page = 1, resultsPerPage = 20 }) {
    logger.info('Generating realistic fallback jobs for Indian market');
    
    const allJobs = this.getAllRealisticJobs();
    
    // Filter by keywords if provided
    let filteredJobs = allJobs;
    if (keywords) {
      const keywordLower = keywords.toLowerCase();
      filteredJobs = allJobs.filter(job => 
        job.title.toLowerCase().includes(keywordLower) ||
        job.company.toLowerCase().includes(keywordLower) ||
        job.description.toLowerCase().includes(keywordLower) ||
        job.skills.some(skill => skill.toLowerCase().includes(keywordLower))
      );
    }
    
    // Filter by location if not 'India'
    if (location && location !== 'India') {
      const locationLower = location.toLowerCase();
      filteredJobs = filteredJobs.filter(job =>
        job.location.toLowerCase().includes(locationLower)
      );
    }
    
    // Sort by date (newest first)
    filteredJobs.sort((a, b) => new Date(b.postedDate) - new Date(a.postedDate));
    
    // Paginate
    const startIndex = (page - 1) * resultsPerPage;
    const endIndex = startIndex + resultsPerPage;
    const paginatedJobs = filteredJobs.slice(startIndex, endIndex);
    
    return {
      jobs: paginatedJobs,
      total: filteredJobs.length,
      page: page,
      totalPages: Math.ceil(filteredJobs.length / resultsPerPage),
    };
  }

